package org.jboss.tools.jst.angularjs.internal.ionic.palette.wizard;

import org.jboss.tools.common.model.XModelObjectConstants;
import org.jboss.tools.common.model.ui.editors.dnd.DropWizardMessages;
import org.jboss.tools.jst.web.ui.JSTWebUIImages;

/**
 * 
 * @author Viacheslav Kabanovich
 *
 */
public class AddJSCSSREferencesWizard extends NewIonicWidgetWizard<NewIonicWidgetWizardPage> {

	public AddJSCSSREferencesWizard() {
		setWindowTitle(DropWizardMessages.Wizard_Window_Title);
		setDefaultPageImageDescriptor(JSTWebUIImages.getInstance()
				.getOrCreateImageDescriptor(JSTWebUIImages.JS_CSS_IMAGE));
	}

	@Override
	protected String[] generateData() {
		String startText = getCommandProperties().getProperty(XModelObjectConstants.START_TEXT);
		String endText = "";
		return new String[]{startText, endText};
	}
}